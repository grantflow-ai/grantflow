@dataclass
class PerformanceMetrics:
    """Performance metrics for adaptive timeout calculation."""

    complexity_level: ContentComplexity
    criteria_count: int
    actual_duration: float
    predicted_duration: float
    success: bool
    timeout_used: float
    timestamp: float

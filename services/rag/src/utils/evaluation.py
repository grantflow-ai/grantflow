class AdaptiveTimeoutCalculator:
    """Calculates adaptive timeouts based on historical performance."""

    def __init__(self, learning_rate: float = TIMEOUT_LEARNING_RATE) -> None:
        self.learning_rate = learning_rate
        self.performance_history: list[PerformanceMetrics] = []
        self.timeout_multipliers: dict[ContentComplexity, float] = {
            ContentComplexity.SIMPLE: 1.0,
            ContentComplexity.MODERATE: 1.0,
            ContentComplexity.COMPLEX: 1.0,
            ContentComplexity.VERY_COMPLEX: 1.0,
        }

    def calculate_adaptive_timeout(
        self,
        base_timeout: float,
        complexity_level: ContentComplexity,
        criteria_count: int,
    ) -> float:
        """Calculate adaptive timeout based on historical performance."""

        multiplier = self.timeout_multipliers.get(complexity_level, 1.0)

        criteria_adjustment = 1.0 + (criteria_count - 1) * 0.15

        adaptive_timeout = base_timeout * multiplier * criteria_adjustment

        min_timeout = base_timeout * MIN_TIMEOUT_MULTIPLIER
        max_timeout = base_timeout * MAX_TIMEOUT_MULTIPLIER

        return max(min_timeout, min(adaptive_timeout, max_timeout))

    def record_performance(self, metrics: PerformanceMetrics) -> None:
        """Record performance metrics and update timeout multipliers."""
        self.performance_history.append(metrics)

        if len(self.performance_history) > PERFORMANCE_HISTORY_SIZE:
            self.performance_history = self.performance_history[-PERFORMANCE_HISTORY_SIZE:]

        self._update_timeout_multiplier(metrics)

    def _update_timeout_multiplier(self, metrics: PerformanceMetrics) -> None:
        """Update timeout multiplier based on performance metrics."""
        complexity_level = metrics.complexity_level
        current_multiplier = self.timeout_multipliers[complexity_level]

        if metrics.predicted_duration > 0:
            performance_ratio = metrics.actual_duration / metrics.predicted_duration
        else:
            performance_ratio = 1.0

        if metrics.success:
            if performance_ratio < 0.7:
                adjustment = -self.learning_rate * 0.5
            elif performance_ratio > 1.2:
                adjustment = self.learning_rate * 0.3
            else:
                adjustment = 0.0
        else:
            adjustment = self.learning_rate * min(performance_ratio, 2.0)

        new_multiplier = current_multiplier + adjustment
        new_multiplier = max(MIN_TIMEOUT_MULTIPLIER, min(new_multiplier, MAX_TIMEOUT_MULTIPLIER))

        self.timeout_multipliers[complexity_level] = new_multiplier

        logger.debug(
            "Updated timeout multiplier",
            complexity_level=complexity_level.value,
            old_multiplier=current_multiplier,
            new_multiplier=new_multiplier,
            performance_ratio=performance_ratio,
            success=metrics.success,
        )

    def get_performance_stats(self) -> dict[str, Any]:
        """Get performance statistics for monitoring."""
        if not self.performance_history:
            return {"total_evaluations": 0}

        recent_metrics = self.performance_history[-50:]

        success_rate = sum(1 for m in recent_metrics if m.success) / len(recent_metrics)
        avg_duration = sum(m.actual_duration for m in recent_metrics) / len(recent_metrics)
        avg_prediction_accuracy = sum(
            abs(m.actual_duration - m.predicted_duration) / max(m.predicted_duration, 0.1) for m in recent_metrics
        ) / len(recent_metrics)

        complexity_stats = {}
        for complexity in ContentComplexity:
            complexity_metrics = [m for m in recent_metrics if m.complexity_level == complexity]
            if complexity_metrics:
                complexity_stats[complexity.value] = {
                    "count": len(complexity_metrics),
                    "success_rate": sum(1 for m in complexity_metrics if m.success) / len(complexity_metrics),
                    "avg_duration": sum(m.actual_duration for m in complexity_metrics) / len(complexity_metrics),
                    "timeout_multiplier": self.timeout_multipliers[complexity],
                }

        return {
            "total_evaluations": len(self.performance_history),
            "recent_evaluations": len(recent_metrics),
            "success_rate": success_rate,
            "avg_duration": avg_duration,
            "prediction_accuracy": 1.0 - min(avg_prediction_accuracy, 1.0),
            "complexity_stats": complexity_stats,
            "timeout_multipliers": {k.value: v for k, v in self.timeout_multipliers.items()},
        }


@dataclass
class CacheEntry:
    """Cache entry with timestamp for TTL management."""

    result: EvaluationToolResponse
    timestamp: float
    access_count: int = 0


class EvaluationCache:
    """LRU cache for evaluation results with TTL and content-based hashing."""

    def __init__(self, max_size: int = DEFAULT_CACHE_SIZE, ttl_seconds: float = DEFAULT_TTL_SECONDS) -> None:
        self.max_size = max_size
        self.ttl_seconds = ttl_seconds
        self._cache: dict[str, CacheEntry] = {}
        self._access_order: list[str] = []

    def _generate_cache_key(
        self, prompt: str, model_output: str | dict[str, Any], criteria: list[EvaluationCriterion]
    ) -> str:
        """Generate a deterministic cache key based on evaluation inputs."""
        content = f"{prompt}|{model_output}|{[c.name for c in criteria]}"
        return hashlib.sha256(content.encode()).hexdigest()[:32]

    def _is_expired(self, entry: CacheEntry) -> bool:
        """Check if cache entry has expired."""
        return time.time() - entry.timestamp > self.ttl_seconds

    def _evict_expired(self) -> None:
        """Remove expired entries from cache."""
        current_time = time.time()
        expired_keys = [key for key, entry in self._cache.items() if current_time - entry.timestamp > self.ttl_seconds]

        for key in expired_keys:
            self._remove_entry(key)

    def _remove_entry(self, key: str) -> None:
        """Remove entry from cache and access order."""
        if key in self._cache:
            del self._cache[key]
        if key in self._access_order:
            self._access_order.remove(key)

    def _evict_lru(self) -> None:
        """Evict least recently used entry."""
        if self._access_order:
            lru_key = self._access_order[0]
            self._remove_entry(lru_key)

    def _update_access_order(self, key: str) -> None:
        """Update access order for LRU tracking."""
        if key in self._access_order:
            self._access_order.remove(key)
        self._access_order.append(key)

    def get(
        self, prompt: str, model_output: str | dict[str, Any], criteria: list[EvaluationCriterion]
    ) -> EvaluationToolResponse | None:
        """Get cached evaluation result if available and not expired."""
        key = self._generate_cache_key(prompt, model_output, criteria)

        if key not in self._cache:
            return None

        entry = self._cache[key]

        if self._is_expired(entry):
            self._remove_entry(key)
            return None

        entry.access_count += 1
        self._update_access_order(key)

        logger.debug(
            "Cache hit for evaluation",
            cache_key=key[:8],
            access_count=entry.access_count,
            age_seconds=time.time() - entry.timestamp,
        )

        return entry.result

    def put(
        self,
        prompt: str,
        model_output: str | dict[str, Any],
        criteria: list[EvaluationCriterion],
        result: EvaluationToolResponse,
    ) -> None:
        """Store evaluation result in cache."""

        self._evict_expired()

        while len(self._cache) >= self.max_size:
            self._evict_lru()

        key = self._generate_cache_key(prompt, model_output, criteria)
        entry = CacheEntry(
            result=result,
            timestamp=time.time(),
        )

        self._cache[key] = entry
        self._update_access_order(key)

        logger.debug(
            "Cached evaluation result",
            cache_key=key[:8],
            cache_size=len(self._cache),
        )

    def clear(self) -> None:
        """Clear all cached entries."""
        self._cache.clear()
        self._access_order.clear()

    def stats(self) -> dict[str, Any]:
        """Get cache statistics."""
        total_access_count = sum(entry.access_count for entry in self._cache.values())

        return {
            "size": len(self._cache),
            "max_size": self.max_size,
            "total_accesses": total_access_count,
            "ttl_seconds": self.ttl_seconds,
        }



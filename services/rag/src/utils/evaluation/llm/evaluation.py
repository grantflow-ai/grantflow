async def evaluate_prompt_output(
    *, criteria: list[EvaluationCriterion], prompt: str, model_output: str | dict[str, Any], trace_id: str
) -> EvaluationToolResponse:
    cached_result = _evaluation_cache.get(prompt, model_output, criteria)
    if cached_result is not None:
        return cached_result

    criteria_text = """
    ## Evaluation Criteria
    """

    for criterion in criteria:
        criteria_text += f"""
        ### {criterion.name}
        {criterion.evaluation_instructions}
        """

    criteria_text += """

    ## Output Instructions:

    Respond with a structured evaluation for each criterion.
    """

    json_schema = {
        "type": "object",
        "properties": {
            "criteria": {
                "type": "object",
                "properties": {criterion.name: score_object_schema for criterion in criteria},
                "required": [criterion.name for criterion in criteria],
            },
        },
        "required": ["criteria"],
    }

    result = await make_google_completions_request(
        prompt_identifier="evaluation_prompt_output",
        response_type=EvaluationToolResponse,
        response_schema=json_schema,
        system_prompt=EVALUATION_SYSTEM_PROMPT,
        model=EVALUATION_MODEL,
        messages=EVALUATION_PROMPT.to_string(
            prompt=prompt, model_output=model_output, missing_info_format=MISSING_INFO_FORMAT
        ),
        temperature=0.2,
        top_p=0.7,
        trace_id=trace_id,
    )

    _evaluation_cache.put(prompt, model_output, criteria, result)

    return result


def analyze_content_complexity(content: str, criteria: list[EvaluationCriterion] | None = None) -> ComplexityAnalysis:
    words = content.split()
    word_count = len(words)
    sentences = re.split(r"[.!?]+", content)
    sentence_count = len([s for s in sentences if s.strip()])

    section_markers = re.findall(r"^#{1,6}\s+.+$", content, re.MULTILINE)
    section_count = len(section_markers)

    technical_patterns = [
        r"\b(?:methodology|hypothesis|analysis|research|study|investigation|assessment|evaluation)\b",
        r"\b(?:statistical|empirical|quantitative|qualitative|experimental|theoretical)\b",
        r"\b(?:correlation|significance|regression|validation|optimization|algorithm)\b",
        r"\b(?:protocol|procedure|framework|implementation|infrastructure|architecture)\b",
        r"\b\w+(?:ology|ism|tion|sion|ance|ence)\b",
    ]

    technical_terms_count = 0
    for pattern in technical_patterns:
        technical_terms_count += len(re.findall(pattern, content, re.IGNORECASE))

    avg_sentence_length = word_count / max(sentence_count, 1)
    avg_syllables = sum(_count_syllables(word) for word in words[:100]) / max(min(len(words), 100), 1)
    readability_score = 206.835 - (1.015 * avg_sentence_length) - (84.6 * avg_syllables)
    readability_score = max(0, min(100, readability_score))

    structural_factors = [
        min(section_count / 5, 1.0),
        min(technical_terms_count / 20, 1.0),
        min(word_count / 1000, 1.0),
        1.0 - (readability_score / 100),
    ]
    structural_complexity = sum(structural_factors) / len(structural_factors)

    complexity_level = _determine_complexity_level(
        word_count, technical_terms_count, section_count, structural_complexity
    )

    recommended_timeout, recommended_mode = _get_routing_recommendations(
        complexity_level, criteria, use_adaptive_timeout=True
    )

    confidence_factors = [
        min(word_count / 50, 1.0),
        min(sentence_count / 10, 1.0),
        1.0 if section_count > 0 else 0.5,
    ]
    confidence_score = sum(confidence_factors) / len(confidence_factors)

    analysis_details = {
        "avg_sentence_length": avg_sentence_length,
        "avg_syllables_per_word": avg_syllables,
        "technical_density": technical_terms_count / max(word_count, 1),
        "section_density": section_count / max(word_count / 100, 1),
        "structural_factors": {
            "section_organization": structural_factors[0],
            "technical_density": structural_factors[1],
            "length_complexity": structural_factors[2],
            "readability_inverse": structural_factors[3],
        },
    }

    logger.debug(
        "Content complexity analysis completed",
        complexity_level=complexity_level.value,
        word_count=word_count,
        technical_terms=technical_terms_count,
        recommended_timeout=recommended_timeout,
        confidence_score=confidence_score,
    )

    return ComplexityAnalysis(
        complexity_level=complexity_level,
        word_count=word_count,
        sentence_count=sentence_count,
        section_count=section_count,
        technical_terms_count=technical_terms_count,
        readability_score=readability_score,
        structural_complexity=structural_complexity,
        recommended_timeout=recommended_timeout,
        recommended_evaluation_mode=recommended_mode,
        confidence_score=confidence_score,
        analysis_details=analysis_details,
    )


def _count_syllables(word: str) -> float:
    word = word.lower().strip()
    if not word:
        return 0

    vowels = "aeiouy"
    syllable_count = 0
    prev_was_vowel = False

    for char in word:
        is_vowel = char in vowels
        if is_vowel and not prev_was_vowel:
            syllable_count += 1
        prev_was_vowel = is_vowel

    if word.endswith("e") and syllable_count > 1:
        syllable_count -= 1

    return max(1, syllable_count)


def _determine_complexity_level(
    word_count: int, technical_terms_count: int, section_count: int, structural_complexity: float
) -> ContentComplexity:
    complexity_score = 0.0

    if word_count > COMPLEX_CONTENT_WORD_THRESHOLD:
        complexity_score += 2
    elif word_count > SIMPLE_CONTENT_WORD_THRESHOLD:
        complexity_score += 1

    if technical_terms_count > TECHNICAL_TERMS_THRESHOLD * 2:
        complexity_score += 2
    elif technical_terms_count > TECHNICAL_TERMS_THRESHOLD:
        complexity_score += 1

    if section_count > SECTION_COUNT_THRESHOLD * 2:
        complexity_score += 1
    elif section_count > SECTION_COUNT_THRESHOLD:
        complexity_score += 0.5

    complexity_score += structural_complexity * 2

    if complexity_score >= 5:
        return ContentComplexity.VERY_COMPLEX
    if complexity_score >= 3:
        return ContentComplexity.COMPLEX
    if complexity_score >= 1.5:
        return ContentComplexity.MODERATE
    return ContentComplexity.SIMPLE


def _get_routing_recommendations(
    complexity_level: ContentComplexity,
    criteria: list[EvaluationCriterion] | None,
    use_adaptive_timeout: bool = True,
) -> tuple[float, str]:
    criteria_count = len(criteria) if criteria else 1

    base_timeouts = {
        ContentComplexity.SIMPLE: FAST_EVALUATION_TIMEOUT,
        ContentComplexity.MODERATE: STANDARD_EVALUATION_TIMEOUT,
        ContentComplexity.COMPLEX: COMPREHENSIVE_EVALUATION_TIMEOUT,
        ContentComplexity.VERY_COMPLEX: COMPREHENSIVE_EVALUATION_TIMEOUT * 1.5,
    }

    evaluation_modes = {
        ContentComplexity.SIMPLE: "quick_evaluation",
        ContentComplexity.MODERATE: "standard_evaluation",
        ContentComplexity.COMPLEX: "thorough_evaluation",
        ContentComplexity.VERY_COMPLEX: "prompt_evaluation",
    }

    base_timeout = base_timeouts[complexity_level]

    if use_adaptive_timeout:
        recommended_timeout = _adaptive_timeout_calculator.calculate_adaptive_timeout(
            base_timeout, complexity_level, criteria_count
        )
    else:
        timeout_adjustment = 1.0 + (criteria_count - 1) * 0.2
        recommended_timeout = base_timeout * timeout_adjustment

    return recommended_timeout, evaluation_modes[complexity_level]


async def smart_evaluate_output(
    *,
    criteria: list[EvaluationCriterion],
    prompt: str,
    model_output: str | dict[str, Any],
    trace_id: str,
    auto_route: bool = True,
    force_mode: str | None = None,
    force_timeout: float | None = None,
    record_performance: bool = True,
) -> tuple[EvaluationToolResponse, ComplexityAnalysis]:
    start_time = time.time()

    content_str = str(model_output)
    complexity_analysis = analyze_content_complexity(content_str, criteria)

    if force_mode:
        evaluation_mode = force_mode
        timeout = force_timeout or complexity_analysis.recommended_timeout
    elif auto_route:
        evaluation_mode = complexity_analysis.recommended_evaluation_mode
        timeout = force_timeout or complexity_analysis.recommended_timeout
    else:
        evaluation_mode = "standard_evaluation"
        timeout = force_timeout or STANDARD_EVALUATION_TIMEOUT

    predicted_duration = complexity_analysis.recommended_timeout

    logger.info(
        "Smart evaluation routing",
        complexity_level=complexity_analysis.complexity_level.value,
        evaluation_mode=evaluation_mode,
        timeout=timeout,
        predicted_duration=predicted_duration,
        word_count=complexity_analysis.word_count,
        technical_terms=complexity_analysis.technical_terms_count,
        confidence=complexity_analysis.confidence_score,
        trace_id=trace_id,
    )

    success = True
    try:
        if evaluation_mode == "quick_evaluation":
            result = await quick_evaluation(criteria, prompt, model_output, trace_id)
        elif evaluation_mode == "thorough_evaluation":
            result = await thorough_evaluation(criteria, prompt, model_output, trace_id)
        elif evaluation_mode == "prompt_evaluation":
            result = await evaluate_output(
                criteria=criteria,
                prompt=prompt,
                model_output=model_output,
                evaluation_timeout=timeout,
                trace_id=trace_id,
            )
        else:
            result = await standard_evaluation(criteria, prompt, model_output, trace_id)
    except (EvaluationError, TimeoutError) as e:
        success = False
        logger.warning(
            "Smart evaluation failed",
            error=str(e),
            complexity_level=complexity_analysis.complexity_level.value,
            evaluation_mode=evaluation_mode,
            timeout=timeout,
        )
        raise
    finally:
        if record_performance:
            actual_duration = time.time() - start_time
            content_hash = hashlib.sha256(content_str.encode()).hexdigest()[:16]

            metrics = PerformanceMetrics(
                complexity_level=complexity_analysis.complexity_level,
                criteria_count=len(criteria),
                actual_duration=actual_duration,
                predicted_duration=predicted_duration,
                success=success,
                timeout_used=timeout,
                timestamp=time.time(),
                content_hash=content_hash,
            )

            _adaptive_timeout_calculator.record_performance(metrics)

    return result, complexity_analysis


async def evaluate_output(
    *,
    criteria: list[EvaluationCriterion],
    prompt: str,
    model_output: str | dict[str, Any],
    trace_id: str,
    evaluation_timeout: float = FAST_EVALUATION_TIMEOUT,
) -> EvaluationToolResponse:
    start_time = time.time()

    try:
        result = await asyncio.wait_for(
            evaluate_prompt_output(
                criteria=criteria,
                prompt=prompt,
                model_output=model_output,
                trace_id=trace_id,
            ),
            timeout=evaluation_timeout,
        )

        duration = time.time() - start_time
        logger.info(
            "Fast evaluation completed",
            duration_seconds=duration,
            criteria_count=len(criteria),
            timeout_used=evaluation_timeout,
        )

        return result

    except TimeoutError:
        duration = time.time() - start_time
        logger.warning(
            "Evaluation timed out",
            duration_seconds=duration,
            timeout_seconds=evaluation_timeout,
            criteria_count=len(criteria),
        )
        raise EvaluationError(
            f"Evaluation timed out after {evaluation_timeout}s",
            context={
                "duration": duration,
                "timeout": evaluation_timeout,
                "criteria_count": len(criteria),
            },
        ) from None
    except Exception as e:
        duration = time.time() - start_time
        logger.error(
            "Evaluation failed",
            duration_seconds=duration,
            error=str(e),
            criteria_count=len(criteria),
        )
        raise EvaluationError(
            f"Evaluation failed: {e}",
            context={
                "duration": duration,
                "original_error": str(e),
                "criteria_count": len(criteria),
            },
        ) from e


async def prompt_evaluation[T](
    *,
    prompt_identifier: str,
    passing_score: int = 70,
    prompt: str,
    prompt_handler: Any,
    retries: int = 2,
    increment: float = 10.0,
    criteria: list[EvaluationCriterion],
    trace_id: str,
    timeout_per_attempt: float = STANDARD_EVALUATION_TIMEOUT,
    early_termination: bool = True,
    **kwargs: Any,
) -> T:
    start_time = time.time()
    current_prompt = str(prompt)
    iteration = 1
    min_passing_score = float(passing_score)

    mapped_criteria = {criterion.name: criterion for criterion in criteria}
    failures: list[dict[str, Any]] = []

    logger.info(
        "Starting optimized evaluation",
        prompt_identifier=prompt_identifier,
        passing_score=min_passing_score,
        max_retries=retries,
        timeout_per_attempt=timeout_per_attempt,
        early_termination=early_termination,
        trace_id=trace_id,
    )

    while iteration <= retries + 1:
        iteration_start = time.time()

        try:
            model_output = await prompt_handler(current_prompt, **kwargs)

            evaluation_result = await evaluate_output(
                criteria=criteria,
                prompt=current_prompt,
                model_output=model_output,
                evaluation_timeout=timeout_per_attempt,
                trace_id=trace_id,
            )

            failing_criteria = {}
            excellent_scores = 0
            total_weighted_score = 0.0
            total_weight = 0.0

            for criterion_name, result in evaluation_result["criteria"].items():
                if criterion_name in mapped_criteria:
                    criterion = mapped_criteria[criterion_name]
                    score = result["score"]

                    total_weighted_score += score * criterion.weight
                    total_weight += criterion.weight

                    if score < min_passing_score:
                        failing_criteria[criterion_name] = result
                    elif score >= EXCELLENT_SCORE_THRESHOLD:
                        excellent_scores += 1

            overall_score = total_weighted_score / total_weight if total_weight > 0 else 0

            if not failing_criteria:
                total_duration = time.time() - start_time
                logger.info(
                    "Evaluation passed",
                    prompt_identifier=prompt_identifier,
                    iteration=iteration,
                    overall_score=overall_score,
                    total_duration=total_duration,
                    trace_id=trace_id,
                )
                return cast("T", model_output)

            if (
                early_termination
                and excellent_scores >= len(criteria) // 2
                and overall_score >= min_passing_score * 0.9
            ):
                total_duration = time.time() - start_time
                logger.info(
                    "Early termination - good enough",
                    prompt_identifier=prompt_identifier,
                    iteration=iteration,
                    overall_score=overall_score,
                    excellent_scores=excellent_scores,
                    total_duration=total_duration,
                    trace_id=trace_id,
                )
                return cast("T", model_output)

            iteration_duration = time.time() - iteration_start
            logger.warning(
                "Evaluation failed - preparing retry",
                prompt_identifier=prompt_identifier,
                iteration=iteration,
                overall_score=overall_score,
                iteration_duration=iteration_duration,
                failing_criteria_count=len(failing_criteria),
            )

            failures.append(
                {
                    "iteration": iteration,
                    "overall_score": overall_score,
                    "failing_criteria": {k: v["score"] for k, v in failing_criteria.items()},
                    "duration": iteration_duration,
                }
            )

            iteration += 1

            if iteration > retries + 1 or min_passing_score - increment < MIN_PASSING_SCORE:
                logger.info(
                    "Evaluation complete - returning result",
                    prompt_identifier=prompt_identifier,
                    iteration=iteration - 1,
                    retries=retries,
                    reason="exhausted_retries" if iteration > retries else "at_minimum_score",
                    current_passing_score=min_passing_score,
                    all_scores={k: v["score"] for k, v in evaluation_result["criteria"].items()},
                )
                return cast("T", model_output)

            min_passing_score -= increment

            improvement_instructions = []
            for criterion_name, result in failing_criteria.items():
                improvement_instructions.append(f"- {criterion_name}: {result['instructions']}")

            current_prompt = f"""
            Improve the previous output based on this feedback:
            {chr(10).join(improvement_instructions)}

            Original prompt: {prompt}
            Previous output: {model_output}

            Generate an improved version that addresses the feedback above.
            """

        except EvaluationError:
            raise
        except Exception as e:
            iteration_duration = time.time() - iteration_start
            logger.exception(
                "Unexpected error in evaluation iteration",
                prompt_identifier=prompt_identifier,
                iteration=iteration,
                error=str(e),
                iteration_duration=iteration_duration,
            )
            failures.append(
                {
                    "iteration": iteration,
                    "error": str(e),
                    "duration": iteration_duration,
                }
            )
            iteration += 1

    total_duration = time.time() - start_time
    logger.warning(
        "Exhausted retries, returning final result instead of failing",
        prompt_identifier=prompt_identifier,
        total_duration=total_duration,
        failures_count=len(failures),
        final_passing_score=min_passing_score,
    )

    return cast("T", model_output) if "model_output" in locals() else await prompt_handler(current_prompt, **kwargs)


async def batch_evaluate_outputs(
    evaluation_tasks: list[dict[str, Any]],
    max_concurrent: int = 3,
    timeout_per_task: float = STANDARD_EVALUATION_TIMEOUT,
) -> list[EvaluationToolResponse | Exception]:
    start_time = time.time()

    async def evaluate_single_task(task: dict[str, Any]) -> EvaluationToolResponse | Exception:
        try:
            return await evaluate_output(
                criteria=task["criteria"],
                prompt=task["prompt"],
                model_output=task["model_output"],
                trace_id=task["trace_id"],
                evaluation_timeout=timeout_per_task,
            )
        except (EvaluationError, TimeoutError) as e:
            return e

    semaphore = asyncio.Semaphore(max_concurrent)

    async def limited_evaluate(task: dict[str, Any]) -> EvaluationToolResponse | Exception:
        async with semaphore:
            return await evaluate_single_task(task)

    raw_results = await asyncio.gather(
        *[limited_evaluate(task) for task in evaluation_tasks],
        return_exceptions=True,
    )

    results: list[EvaluationToolResponse | Exception] = []
    for result in raw_results:
        if isinstance(result, Exception):
            results.append(result)
        elif isinstance(result, BaseException):
            results.append(Exception(str(result)))
        elif hasattr(result, "criteria"):
            results.append(result)
        else:
            results.append(Exception(f"Unexpected result type: {type(result)}"))

    duration = time.time() - start_time
    successful_evaluations = sum(1 for r in results if not isinstance(r, Exception))

    logger.info(
        "Batch evaluation completed",
        total_tasks=len(evaluation_tasks),
        successful_evaluations=successful_evaluations,
        failed_evaluations=len(evaluation_tasks) - successful_evaluations,
        duration_seconds=duration,
        max_concurrent=max_concurrent,
    )

    return results


async def with_evaluation[T, **P](
    *,
    prompt_identifier: str,
    passing_score: int = 100,
    prompt: str,
    prompt_handler: Callable[P, Awaitable[T]],
    retries: int = 4,
    increment: float = 2.5,
    criteria: list[EvaluationCriterion],
    trace_id: str,
    job_manager: Any | None = None,
    context: "EvaluationContext | None" = None,
    settings: "EvaluationSettings | None" = None,
    evaluator: Callable[[str, str | dict[str, Any], list[EvaluationCriterion], str], Awaitable[EvaluationToolResponse]]
    | None = None,
    **kwargs: Any,
) -> T:
    """Unified evaluation entry point with NLP-based evaluation + optional LLM fallback.

    This function implements a two-phase evaluation strategy:
    1. NLP Evaluation: Uses deterministic NLP metrics for quick assessment
    2. LLM Evaluation (fallback): Criteria-based scoring with iterative improvement

    Args:
        prompt_identifier: Identifier for tracking and logging
        passing_score: Initial LLM passing score (decreases by increment each retry)
        prompt: Initial prompt for content generation
        prompt_handler: Async function that generates content
        retries: Maximum number of LLM evaluation retries
        increment: Amount to decrease passing_score each retry
        criteria: List of evaluation criteria for LLM evaluation
        trace_id: Unique trace ID for logging
        job_manager: Optional job manager for retry tracking
        context: Evaluation context (section config, RAG context, objectives, etc.)
        settings: Evaluation settings controlling fast/LLM routing
        evaluator: Optional custom evaluator function
        **kwargs: Additional arguments passed to prompt_handler

    Returns:
        Generated content of type T (passes evaluation or exhausts retries)

    Raises:
        EvaluationError: If evaluation fails critically
    """

    eval_context = context or {}
    eval_settings = settings or {}

    # Check if NLP evaluation is enabled
    enable_nlp = eval_settings.get("enable_nlp_evaluation", True)
    force_llm = eval_settings.get("force_llm_evaluation", False)

    current_prompt = str(prompt)
    iteration = 1
    min_passing_score: float = passing_score
    mapped_criteria = {criterion.name: criterion for criterion in criteria}
    nlp_eval_feedback: list[str] = []

    logger.info(
        "Starting unified evaluation",
        prompt_identifier=prompt_identifier,
        enable_nlp_evaluation=enable_nlp,
        force_llm_evaluation=force_llm,
        has_context=bool(eval_context),
        trace_id=trace_id,
    )

    while iteration <= retries:
        logger.info(
            "Evaluation iteration",
            prompt_identifier=prompt_identifier,
            iteration=iteration,
            max_retries=retries,
            passing_score=min_passing_score,
            trace_id=trace_id,
        )

        # Generate content
        model_output = await prompt_handler(current_prompt, trace_id=trace_id, **kwargs)  # type: ignore[arg-type]
        output_str = str(model_output)

        # Phase 1: NLP Evaluation (if enabled and not forced to use LLM)
        if enable_nlp and not force_llm and iteration == 1:
            section_config = eval_context.get("section_config")
            if section_config and isinstance(section_config, dict):
                try:
                    logger.info(
                        "Running NLP evaluation",
                        content_length=len(output_str),
                        section_id=section_config.get("id"),
                        trace_id=trace_id,
                    )

                    rag_context = eval_context.get("rag_context", [])
                    if not isinstance(rag_context, list):
                        rag_context = []

                    # Ensure all items are DocumentDTO
                    valid_rag_context: list[DocumentDTO] = [
                        item for item in rag_context if isinstance(item, dict) and "content" in item
                    ]

                    research_objectives = eval_context.get("research_objectives", [])
                    if not isinstance(research_objectives, list):
                        research_objectives = []

                    nlp_result = await evaluate_scientific_content(
                        content=output_str,
                        section_config=section_config,
                        rag_context=valid_rag_context,
                        research_objectives=research_objectives,
                        trace_id=f"{trace_id}_nlp",
                        reference_corpus=eval_context.get("reference_corpus"),
                    )

                    nlp_score = nlp_result["overall_score"] / 100.0
                    nlp_confidence = nlp_result["confidence_score"]
                    nlp_recommendation = nlp_result["recommendation"]

                    confidence_threshold = eval_settings.get("nlp_confidence_threshold", 0.8)
                    accept_threshold = eval_settings.get("nlp_accept_threshold", 85.0) / 100.0

                    logger.info(
                        "NLP evaluation completed",
                        score=nlp_score,
                        confidence=nlp_confidence,
                        recommendation=nlp_recommendation,
                        confidence_threshold=confidence_threshold,
                        trace_id=trace_id,
                    )

                    # Store feedback for later use in LLM improvement
                    nlp_eval_feedback = nlp_result.get("detailed_feedback", [])

                    # Accept if high confidence and good score
                    if nlp_confidence >= confidence_threshold and nlp_score >= accept_threshold:
                        logger.info(
                            "NLP evaluation accepted content - skipping LLM evaluation",
                            nlp_score=nlp_score,
                            nlp_confidence=nlp_confidence,
                            trace_id=trace_id,
                        )
                        return model_output

                    # Reject immediately if score is too low (don't waste LLM calls)
                    if nlp_score < MINIMAL_THRESHOLD:
                        logger.warning(
                            "NLP evaluation rejected content - below minimal threshold",
                            nlp_score=nlp_score,
                            minimal_threshold=MINIMAL_THRESHOLD,
                            trace_id=trace_id,
                        )
                        # Use NLP evaluation feedback to improve
                        content_type = detect_content_type(section_config)
                        improvement_instructions = _generate_improvement_instructions(
                            nlp_result,
                            content_type,
                        )

                        if improvement_instructions:
                            current_prompt = FIX_OUTPUT_PROMPT.to_string(
                                instructions="\n".join([f"  - {instr}" for instr in improvement_instructions]),
                                model_output=model_output,
                                prompt=prompt,
                                missing_info_format=MISSING_INFO_FORMAT,
                            )
                            iteration += 1
                            continue

                    # Falls through to LLM evaluation for borderline cases
                    logger.info(
                        "NLP evaluation uncertain - proceeding to LLM evaluation",
                        nlp_score=nlp_score,
                        nlp_confidence=nlp_confidence,
                        trace_id=trace_id,
                    )

                except Exception as e:
                    logger.warning(
                        "NLP evaluation failed, falling back to LLM",
                        error=str(e),
                        trace_id=trace_id,
                    )

        # Phase 2: LLM Evaluation
        if evaluator:
            evaluation_result = await evaluator(
                current_prompt, cast("dict[str, Any] | str", model_output), criteria, trace_id
            )
        else:
            evaluation_result = await evaluate_prompt_output(
                prompt=current_prompt,
                model_output=cast("dict[str, Any] | str", model_output),
                criteria=criteria,
                trace_id=trace_id,
            )

        failing_criteria = {
            k: v
            for k, v in evaluation_result["criteria"].items()
            if v["score"] < min(min_passing_score * mapped_criteria[k].weight, 100)
        }

        if not failing_criteria:
            logger.info(
                "LLM evaluation passed",
                prompt_identifier=prompt_identifier,
                iteration=iteration,
                all_scores={k: v["score"] for k, v in evaluation_result["criteria"].items()},
                trace_id=trace_id,
            )
            return model_output

        logger.warning(
            "LLM evaluation failed",
            prompt_identifier=prompt_identifier,
            iteration=iteration,
            failing_criteria={k: v["score"] for k, v in failing_criteria.items()},
            all_scores={k: v["score"] for k, v in evaluation_result["criteria"].items()},
            trace_id=trace_id,
        )

        if job_manager and hasattr(job_manager, "increment_retry_count"):
            retry_count = await job_manager.increment_retry_count()
            logger.debug(
                "Job retry count incremented",
                prompt_identifier=prompt_identifier,
                iteration=iteration,
                job_retry_count=retry_count,
                trace_id=trace_id,
            )

        min_passing_score -= increment
        iteration += 1

        if min_passing_score <= MIN_PASSING_SCORE:
            logger.warning(
                "Reached minimum passing score, returning result",
                prompt_identifier=prompt_identifier,
                iteration=iteration,
                current_passing_score=min_passing_score,
                minimum_threshold=MIN_PASSING_SCORE,
                all_scores={k: v["score"] for k, v in evaluation_result["criteria"].items()},
                trace_id=trace_id,
            )
            return model_output

        # Build improvement prompt combining LLM criteria feedback + NLP evaluation feedback
        llm_instructions = [
            f"  - {criterion.name}: {result['instructions']}"
            for criterion, result in [(mapped_criteria[k], v) for k, v in failing_criteria.items()]
        ]

        # Include NLP evaluation feedback if available
        all_instructions = llm_instructions
        if nlp_eval_feedback:
            all_instructions.append("\n  Additional Context from NLP Evaluation:")
            all_instructions.extend([f"    - {feedback}" for feedback in nlp_eval_feedback[:5]])

        current_prompt = FIX_OUTPUT_PROMPT.to_string(
            instructions="\n".join(all_instructions),
            model_output=model_output,
            prompt=prompt,
            missing_info_format=MISSING_INFO_FORMAT,
        )

    logger.warning(
        "Exhausted retries, returning final result",
        prompt_identifier=prompt_identifier,
        retries=retries,
        min_passing_score=min_passing_score,
        trace_id=trace_id,
    )
    return model_output



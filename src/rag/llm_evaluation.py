EVALUATION_SYSTEM_PROMPT = """
You are an expert evaluator of language model outputs. Your task is to rigorously assess the quality of generated outputs based on a set of well-defined criteria. Provide detailed feedback and actionable improvement instructions.
"""

EVALUATION_PROMPT = PromptTemplate(
    name="evaluation_prompt_output",
    template="""
    Your task is to evaluate the output generated by a language model in response to a specific prompt.

    ### Inputs:
    The prompt:
      <prompt>
      ${prompt}
      </prompt>

    The model output:
      <model_output>
      ${model_output}
      </model_output>

    Guidelines:
        1. Begin by analyzing the prompt to understand the requirements, guidelines and any provided validation rules.
        2. Evaluate the output generated by the language model relative to the instructions and sources available in the prompt.
        3. Evaluate the output against the evaluation criteria provided factoring the quality and availability of sources in the prompt.
        4. Assign scores based on the quality of the output: 0 is the worst and 100 is the best, offsetting for missing source data in the prompt.
        5. Provide concrete instructions for improvement, if the sources provided in the prompt are insufficient or the output is inaccurate.
    """,
)


@dataclass
class EvaluationCriterion:
    """Structured score and reasoning for a single evaluation criterion."""

    name: str
    """The name of the evaluation criterion."""
    evaluation_instructions: str
    """Instructions for evaluating the criterion."""
    weight: float = 1.0
    """The weight of the criterion in the overall evaluation."""


class EvaluationScore(TypedDict):
    """Structured score and reasoning for a single evaluation criterion."""

    score: int
    """The score assigned to the output."""
    instructions: str
    """Instructions for improving the output."""


class EvaluationToolResponse(TypedDict):
    """Comprehensive evaluation response."""

    criteria: dict[str, EvaluationScore]
    """Structured scores and reasoning for each evaluation criterion."""


score_object_schema = {
    "type": "object",
    "properties": {"score": {"type": "integer", "minimum": 0, "maximum": 100}, "instructions": {"type": "string"}},
    "required": ["score", "instructions"],
}


async def evaluate_prompt_output(
    *, criteria: list[EvaluationCriterion], prompt: str, model_output: str | dict[str, Any]
) -> EvaluationToolResponse:
    """Generate an evaluation prompt for assessing the quality of a language model output.

    Args:
        criteria: The evaluation criteria to use.
        prompt: The prompt given to the language model.
        model_output: The generated output from the language model.

    Returns:
        The evaluation result object.
    """
    criteria_text = """
    ## Evaluation Criteria
    """

    for criterion in criteria:
        criteria_text += f"""
        ### {criterion.name}
        {criterion.evaluation_instructions}
        """

    obj = dumps({v.name: {"score": 90, "instructions": "Update the output..."} for v in criteria}, indent=2)

    criteria_text += f"""

    ## Output Instructions:

    Respond with a JSON object like the following:

    {obj}
    """

    json_schema = {
        "type": "object",
        "properties": {
            "criteria": {
                "type": "object",
                "properties": {criterion.name: score_object_schema for criterion in criteria},
                "required": [criterion.name for criterion in criteria],
            },
        },
        "required": ["criteria"],
    }

    return await make_google_completions_request(
        prompt_identifier="evaluation_prompt_output",
        response_type=EvaluationToolResponse,
        response_schema=json_schema,
        system_prompt=EVALUATION_SYSTEM_PROMPT,
        model=EVALUATION_MODEL,
        messages=EVALUATION_PROMPT.to_string(prompt=prompt, model_output=model_output),
        temperature=0.2,
        top_p=0.7,
    )


FIX_OUTPUT_PROMPT: Final[PromptTemplate] = PromptTemplate(
    name="fix_output",
    template="""
    ## Task
    Your task is to update and improve the following model output:
        <model_output>
        ${model_output}
        </model_output>

    This output was generated using the following prompt:
        <prompt>
        ${prompt}
        </prompt>

    ## Instructions:
    Follow these instructions to improve the model output:
        <instructions>
        ${instructions}
        </instructions>

    1. Begin by analyzing the prompt to understand the requirements, guidelines, and any provided validation rules it specifies.
    2. Apply the necessary changes, and ensure that the output meets the requirements and validation criteria.
    3. Respond with the updated model output adhering with the output instruction in the prompt.
    """,
)


async def with_prompt_evaluation[T, **P](
    *,
    prompt_identifier: str,
    passing_score: int = 100,
    prompt: str,
    prompt_handler: Callable[P, Awaitable[T]],
    retries: int = 4,
    increment: float = 2.5,
    criteria: list[EvaluationCriterion],
    **kwargs: Any,
) -> T:
    """Evaluate the output of a language model against a set of criteria and provide feedback.

    Args:
        prompt_identifier: The identifier for the prompt.
        passing_score: The minimum score required to pass the evaluation.
        prompt: The prompt used to generate the output.
        prompt_handler: The handler for generating the output.
        retries: The number of retries allowed.
        increment: The increment for temperature in retries.
        criteria: The evaluation criteria to use.
        **kwargs: Additional keyword arguments.

    Raises:
        EvaluationError: If the output does not meet the evaluation criteria after the maximum number of retries.

    Returns:
        The evaluated output.
    """
    current_prompt = str(prompt)
    iteration = 1
    min_passing_score: float = passing_score

    mapped_criteria = {criterion.name: criterion for criterion in criteria}

    failures: list[dict[str, EvaluationScore]] = []

    while iteration <= retries:
        model_output = await prompt_handler(current_prompt, **kwargs)  # type: ignore[arg-type]
        evaluation_result = await evaluate_prompt_output(
            prompt=current_prompt, model_output=cast(dict[str, Any] | str, model_output), criteria=criteria
        )

        failing_criteria = {
            k: v
            for k, v in evaluation_result["criteria"].items()
            if v["score"] < min(min_passing_score * mapped_criteria[k].weight, 100)
        }

        if not failing_criteria:
            return model_output

        failures.append(failing_criteria)

        min_passing_score -= increment
        iteration += 1

        current_prompt = FIX_OUTPUT_PROMPT.to_string(
            instructions="\n".join(
                [f"  - {dedent(score_object['instructions'])}" for score_object in failing_criteria.values()]
            ),
            model_output=model_output,
            prompt=prompt,
        )

    raise EvaluationError(
        f"Failed to generate an acceptable response after {retries} retries. Please review the evaluation criteria and try again.",
        context={
            "prompt_identifier": prompt_identifier,
            "failures": {i + 1: failure for i, failure in enumerate(failures)},
        },
    )

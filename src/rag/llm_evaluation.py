class EvaluationScore(TypedDict):
    """Structured score and reasoning for a single evaluation criterion."""

    score: int
    """The score assigned to the output."""
    reasoning: str
    """The reasoning for the score."""


class CriteriaObject(TypedDict):
    """Structured score and reasoning for a single evaluation criterion."""

    accuracy: EvaluationScore
    """The accuracy of the output."""
    completeness: EvaluationScore
    """The completeness of the output."""
    correctness: EvaluationScore
    """The correctness of the output."""
    coherence: EvaluationScore
    """The coherence of the output."""
    precision: EvaluationScore
    """The precision of the output."""
    information_density: EvaluationScore
    """The information density of the output."""


class EvaluationToolResponse(TypedDict):
    """Comprehensive evaluation response."""

    scores: CriteriaObject
    """The scores for each evaluation criterion."""
    instructions: str
    """Instructions for improving the output."""


score_object_schema = {
    "type": "object",
    "properties": {"score": {"type": "integer", "minimum": 0, "maximum": 100}, "reasoning": {"type": "string"}},
    "required": ["score", "reasoning"],
}

json_schema = {
    "type": "object",
    "properties": {
        "scores": {
            "type": "object",
            "properties": {
                "accuracy": score_object_schema,
                "completeness": score_object_schema,
                "correctness": score_object_schema,
                "coherence": score_object_schema,
                "precision": score_object_schema,
                "information_density": score_object_schema,
            },
            "required": ["accuracy", "completeness", "correctness", "coherence", "precision", "information_density"],
        },
        "instructions": {"type": "string"},
    },
    "required": [
        "scores",
        "instructions",
    ],
}


async def evaluate_prompt_output(*, prompt: str, model_output: str | dict[str, Any]) -> EvaluationToolResponse:
    """Generate an evaluation prompt for assessing the quality of a language model output.

    Args:
        prompt: The prompt given to the language model.
        model_output: The generated output from the language model.

    Returns:
        The evaluation result object.
    """
    return await make_completions_request(
        prompt_identifier="evaluation_prompt_output",
        response_type=EvaluationToolResponse,
        response_schema=json_schema,
        system_prompt=EVALUATION_SYSTEM_PROMPT,
        messages=EVALUATION_PROMPT.to_string(prompt=prompt, model_output=model_output),
    )


class PromptHandler[T](Protocol):
    """Protocol for a prompt handler function."""

    async def __call__(self, prompt: PromptTemplate | str, **kwargs: Any) -> T: ...  # noqa: D102

    """Handle a prompt and return the output."""


async def with_prompt_evaluation[T, P](
    *,
    passing_score: int = 100,
    prompt: P,
    prompt_handler: Callable[[P], Awaitable[T]],
    retries: int = 4,
) -> T:
    """Prompt the user for an evaluation of the generated text.

    Args:
        passing_score: The minimum score required to pass the evaluation.
        prompt: The prompt to evaluate
        prompt_handler: The prompt handler function.
        retries: The number of retries allowed for the evaluation.

    Raises:
        EvaluationError: If the model output does not meet the evaluation criteria.

    Returns:
        The model output that passed the evaluation.
    """
    current_prompt = str(prompt)

    iteration = 1
    min_passing_score = passing_score

    while iteration <= retries:
        model_output = await prompt_handler(prompt)
        evaluation_result = await evaluate_prompt_output(
            prompt=current_prompt, model_output=cast(dict[str, Any] | str, model_output)
        )
        scores = evaluation_result["scores"]

        if all(v["score"] >= min_passing_score for v in scores.values()):  # type: ignore[index]
            return model_output

        failing_criteria = {k: v["reasoning"] for k, v in scores.items() if v["score"] < min_passing_score}  # type: ignore[index]

        min_passing_score -= round(iteration * 2.5)
        iteration += 1

        current_prompt = dedent(f"""
            Your task is to update and fix the output of a previous API call that failed evaluation.

            Begin by parsing and understanding the original prompt:
                <prompt>
                {prompt}
                </prompt>

            The LLM generated the following output, which you should update:
                <model_output>
                {model_output}
                </model_output>

            The evaluation failed due to the following reasons:
                <evaluation_output>
                {serialize(failing_criteria).decode()}
                </evaluation_output>

            Follow these instructions to fix the output:
                <instructions>
                {evaluation_result["instructions"]}
                </instructions>

            Please begin your analysis and respond with the updated output.
            """)

    raise EvaluationError(
        f"Failed to generate an acceptable response after {retries} retries. Please review the evaluation criteria and try again."
    )
